package com.numbers.jttp.request;

/**
 * an enumeration of all HTTP request methods implemented by the
 * {@link com.numbers.jttp.Jttp} class
 *
 * @author Numbers
 */
public enum HttpRequestType {

    /**
     * HTTP GET
     */
    GET,
    /**
     * HTTP POST
     */
    POST,
    /**
     * HTTP DELETE
     */
    DELETE,
    /**
     * HTTP HEAD
     */
    HEAD,
    /**
     * HTTP PATCH
     */
    PATCH,
    /**
     * HTTP PUT
     */
    PUT
}
